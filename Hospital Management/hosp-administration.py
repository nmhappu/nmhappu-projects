import mysql.connector

print("+-- Welcome To Administrative Block --+")
admin = mysql.connector.connect(
    host="localhost", user="root", passwd="root", database="hospital_management"
)

cursor = admin.cursor()

if admin.is_connected():
    print("Login Successful!")

hos_doctors = """
List of Departments
1. Psychiatrist
2. Cardiologist
3. Orthologist
4. Physician
5. General Surgeon
6. Pediatrician
7. Neurologist
8. Neuro Surgeon
9. Pediatric Surgeon
10. Oncologist
"""

while True:
    print(
        """1. Add New Doctors\n2. List Available Departments\n3. Delete Doctors\n4. Log Out\n"""
    )

    ch = int(input("Enter Your Choice: "))

    if ch == 1:
        name = input("Enter the doctor's name: ")
        gender = input("Enter the doctor's Gender: ")
        dept = input("Enter the doctor's Department: ")
        exp = input("Enter the doctor's Years of Experience: ")
        fee = input("Enter the doctor's Consultation fee: ")

        cursor.execute(
            "INSERT INTO docs (name, gender, dept, exp, fee) VALUES ('{}','{}','{}',{},{})".format(
                name, gender, dept, exp, fee
            )
        )

        admin.commit()
        print("Values Added!\n")

    elif ch == 2:
        print(hos_doctors)

    elif ch == 3:
        name = input("Enter the doctor's name you want to delete: ")

        cursor.execute("DELETE FROM docs WHERE name ='{}'".format(name))

        admin.commit()
        print("Profile deleted.\n")


    elif ch == 4:
        exit()
